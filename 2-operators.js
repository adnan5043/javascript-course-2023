//----------- There are different types of JavaScript operators----------

// Arithmetic Operators i.e. + -, *, /, %, ++, --, **
// Assignment Operators =, +=, -=, *=, /=, %=, **=
// Comparison Operators ==, ===, != !==, <,>,<=,>=,
// Logical Operators &&, ||, ! 
// Bitwise Operators &, |, ~, ^, <<, >>, >>>
// Ternary Operators iftrue ? "exec" : '';
// Type Operators typeof, instanceof

//----------- There are different types of JavaScript operators----------
let number = 23;
number += 17;
console.log(2+10); // 12
console.log(4*4); // 16
console.log(2**5); // 32
console.log(number); // 40
console.log(5 % 2); // 1 remainder
console.log(4 % 2); // 0
console.log(number == 50); // false
console.log(number == 40 ? 'Amount is valid': 'not valid'); // valid
console.log(typeof number); // number

